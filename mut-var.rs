fn main(){
  let mut a: i8 = 10; //mut ( mutable ) - нужен чтобы изменять переменные
  a = 20; // Задаем новое значение
  println!("a = {}", a); // Выводим новое значение
}

//Если вы скомпилируете код, то компилятор может вас предупредить что 10 нигде не используеться. Это никак не влияет на код, он все равно запуститься!
